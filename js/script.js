$(showLongTextParents).click(function(){
    let longTextParents = document.getElementById ("longTextParents");
  
    if (longTextParents.style.display == "none") longTextParents.style.display = "block";
              else longTextParents.style.display = "none";
  
  });
  
  $(showLongTextAdaptation).click(function(){
    let longTextAdaptation = document.getElementById ("longTextAdaptation");
  
    if (longTextAdaptation.style.display == "none") longTextAdaptation.style.display = "block";
              else longTextAdaptation.style.display = "none";
  
  });